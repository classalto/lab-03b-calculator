// addition function
export function getSum(numberOne, numberTwo) {
  return numberOne + numberTwo;
};

// subtraction function
export function getSubtract(numberOne, numberTwo) {
  return numberOne - numberTwo;
};

// division function
export function getDivision(numberOne, numberTwo) {
  return numberOne / numberTwo;
};

// multiplication function
export function getMultiply(numberOne, numberTwo) {
  return numberOne * numberTwo;
};
